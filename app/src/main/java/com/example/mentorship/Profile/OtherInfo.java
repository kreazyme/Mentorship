package com.example.mentorship.Profile;

public class OtherInfo {
    private int pic;
    private String info;

    public OtherInfo(int pic, String info) {
        this.pic = pic;
        this.info = info;
    }

    public int getPic() {
        return pic;
    }

    public void setPic(int pic) {
        this.pic = pic;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
